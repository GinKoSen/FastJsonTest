public class BA {
	public int b;
	public String a;

	public BA() {
	}

	public BA(int b, String a) {
		super();
		this.b = b;
		this.a = a;
	}

	@Override
	public String toString() {
		return "[b=" + b + ", a=" + a + "]";
	}

}
